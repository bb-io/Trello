    [Action("List cards", Description = "List all board cards")]
    public async Task<ListCardsResponse> ListCards([ActionParameter] BoardCardsFilterRequest input)
    {
        var board = await GetBoardData(input.BoardId);
        board.Cards.Limit = input.Limit ?? 100;
        await board.Cards.Refresh();

        var cards = board.Cards.Select(c => new CardEntity(c)).ToArray();
        return new(cards);
    }
    
    [Action("List assigned cards", Description = "List all cards assigned to the user")]
    public async Task<ListCardsResponse> ListUserCards([ActionParameter] CardFilterRequest request)
    {
        var me = await Client.Me();
        me.Cards.Limit = request.Limit ?? 100;
        
        await me.Cards.Refresh();

        var cards = me.Cards.Select(c => new CardEntity(c)).ToArray();

        return new(cards);
    }

    [Action("Search cards", Description = "Search board cards")]
    public async Task<List<CardEntity>> SearchCards([ActionParameter] SearchCardsRequest input)
    {
        var board = await GetBoardData(input.BoardId);

        var filterApplied = false;
        if (input.CreatedDateFrom.HasValue && input.CreatedDateTo.HasValue)
        {
            board.Cards.Filter(input.CreatedDateFrom.Value, input.CreatedDateTo.Value);
            filterApplied = true;
        }
        
        await board.Cards.Refresh();
        var cards = board.Cards.Select(c => new CardEntity(c)).ToList();
        if (!String.IsNullOrEmpty(input.Name))
        { cards = cards.Where(x => x.Name == input.Name).ToList(); }
        if (!String.IsNullOrEmpty(input.Description))
        { cards = cards.Where(x => x.Description.Contains(input.Description)).ToList(); }
        if (input.CreatedDateFrom != null && !filterApplied)
        { cards = cards.Where(x => x.CreationDate >= input.CreatedDateFrom).ToList(); }
        if (input.CreatedDateTo != null && !filterApplied)
        { cards = cards.Where(x => x.CreationDate <= input.CreatedDateTo).ToList(); }
        if (input.ActivityDateFrom != null)
        { cards = cards.Where(x => x.LastActivity >= input.ActivityDateFrom).ToList(); }
        if (input.ActivityDateTo != null)
        { cards = cards.Where(x => x.LastActivity <= input.ActivityDateTo).ToList(); }

        return cards;
    }

    [Action("Find card", Description = "Find a card by name or url")]
    public async Task<CardEntity> FindCard([ActionParameter] FindCardRequest input)
    {
        if (String.IsNullOrEmpty(input.Name) && String.IsNullOrEmpty(input.Url))
        {
            throw new Exception("Either card name or url need to be specified");
        }
        
        var cards = await RestClient.PaginateCardsAsync(input.BoardId, input.CreatedDateFrom, input.CreatedDateTo);
        if (input.Name != null || input.Url != null)
        {
            foreach (var cardEntity in cards)
            {
                if (input.Name != null && cardEntity.Name == input.Name)
                {
                    var card = new Card(cardEntity.Id);
                    await card.Refresh();
                    await card.List.Refresh();
                    await card.Attachments.Refresh();
                    await card.Comments.Refresh();
                    await card.CheckLists.Refresh();

                    return new CardEntity(card);
                }

                if (input.Url != null && cardEntity.Url == input.Url)
                {
                    var card = new Card(cardEntity.Id);
                    await card.Refresh();
                    await card.List.Refresh();
                    await card.Attachments.Refresh();
                    await card.Comments.Refresh();
                    await card.CheckLists.Refresh();

                    return new CardEntity(card);
                }
            }

            return new();
        }
        
        return new();
    }

    [Action("Get card", Description = "Get specific card details")]
    public async Task<GetCardResponse> GetCard([ActionParameter] CardRequest input)
    {
        var card = new Card(input.CardId);
        await card.Refresh();
        await card.List.Refresh();
        await card.Attachments.Refresh();
        await card.Comments.Refresh();
        await card.CheckLists.Refresh();

        return new GetCardResponse(card);
    }

    [Action("Copy card", Description = "Creates a new card based on another one")]
    public async Task<CardEntity> CopyCard([ActionParameter] ListRequest list, [ActionParameter] CopyCardRequest input)
    {
        var card = new Card(input.CardId);
        await card.Refresh();
        await card.List.Refresh();

        var board = await GetBoardData(list.BoardId);
        await board.Lists.Refresh();

        var CopyOptions = input.GetCopyOptions();

        var newcard = await board.Lists.First(l => l.Id == list.ListId).Cards.Add(card, CopyOptions);

        return new(newcard);
    }


    [Action("Create card", Description = "Create card on board")]
    public async Task<CardEntity> CreateCard(
        [ActionParameter] ListRequest list,
        [ActionParameter] CreateCardRequest input)
    {
        var board = await GetBoardData(list.BoardId);
        await board.Lists.Refresh();

        var card = await board.Lists.First(l => l.Id == list.ListId).Cards
            .Add(input.CardName, input.CardDescription, Position.Top, input.DueDate);

        return new(card);
    }


    [Action("Add attachment to card", Description = "Add an attachment (file or URL) to a card")]
    public async Task<AddAttachmentResponse> AddAttachmentToCard([ActionParameter] AddAttachmentRequest input)
    {
        var card = new Card(input.CardId);
        await card.Refresh();

        IAttachment? attachment;

        if (!string.IsNullOrEmpty(input.Url))
        {
            attachment = await card.Attachments.Add(input.Url, input.Name);
        }
        else if (input.File != null)
        {
            using var fileStream = await _fileManagementClient.DownloadAsync(input.File);

            var fileName = !string.IsNullOrEmpty(input.Name)
                ? input.Name
                : (input.File.Name ?? "Attachment");

            byte[] fileData;
            using (var ms = new MemoryStream())
            {
                await fileStream.CopyToAsync(ms);
                fileData = ms.ToArray();
            }

            attachment = await card.Attachments.Add(fileData, fileName);
        }
        else
        {
            throw new PluginMisconfigurationException("Either 'Url' or 'File' must be provided to add an attachment. Please check your input and try again");
        }

        return new AddAttachmentResponse(attachment);
    }


    [Action("Update card", Description = "Update specific card")]
    public async Task<CardEntity> UpdateCard(
        [ActionParameter] ListCardRequest card,
        [ActionParameter] UpdateCardRequest input)
    {
        try
        {
            var result = new Card(card.CardId)
            {
                Name = input.Name,
                Description = input.Description,
                IsComplete = input.IsComplete,
                IsArchived = input.IsArchived,
                DueDate = input.DueDate
            };

            if (!String.IsNullOrEmpty(input.Comment))
            {
                await result.Comments.Add(input.Comment);
            }



            if (card.ListId is not null)
            {
                var list = new List(card.ListId);
                await list.Refresh();

                result.List = list;
            }

            await result.Refresh();
            return new(result);
        }
        catch (InvalidOperationException ex)
        {
            throw new PluginApplicationException("Error occured while updating card", ex);
        }
        catch (Exception ex)
        {
            throw new PluginApplicationException(ex.Message);
        }
    }
    
    [Action("Delete card", Description = "Delete specific card")]
    public Task DeleteCard([ActionParameter] CardRequest input)
        => new Card(input.CardId).Delete();

    [Action("Add reaction to comment")]
    public async Task ReactToComment([ActionParameter] CommentReactionRequest input)
    {
        Emoji emoji = GetInputEmoji(input.Reaction);

        var card = new Card(input.CardId);
        await card.Comments.Refresh();
        await card.Comments.First(x => x.Data.Text == input.textComment).Reactions.Add(emoji);
        await card.Refresh();
        await card.List.Refresh();

        return;
    }

